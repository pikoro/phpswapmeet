<h2>What is this number next to my username?</h2>

<h3>This is your feedback score.

<p>This number helps promote honest and fair trading between members on the site.</p>

<p>Every member has a feedback score.  It works similar to the score on some largish online auction sites.  For every swap, there is a chance for both members to leave either positive, negative, or neutral feedback about the swap as well as a brief comment.  If someone's score drops below a certain threshold, then their account will be suspended and/or the member will be unable to swap items.</p>

<p>This way, we can help you avoid problem members or people who are trying to swap broken items (for example, a radio that does not work) without listing the item as being such.</p>



