<h1>Frequently Asked Questions</h1>
<h2>What is <?= $config[site][short_name] ?>?</h2>
<h3><?= $config[site][short_name] ?> is an Item Swapping website for people who live in Japan, but specifically on Okinawa.

<p>It is a place where people can get together to swap their unwanted items for things that they want.</p>

<p>We all know what its like to have stuff laying around that we want to get rid of, but wouldn't it be nice to be able to change your unwanted items for things that you really do want?</p>

<p>For example, say you have a video game that you have already played through, or you bought it and turns out it's not something you want to play.  Instead of selling it at a loss (nobody pays full price for anything used) why not trade it with someone else who has a game that you DO want to play.  They get a new game to try out, and you get to turn your unwanted game into something that you do want to play.</p>

<p>This same theory works for anything that you have.  That old TV sitting in your living room taking up too much space, but you really don't feel like trying to sell it since you'll lose money on it?  Why not post the item as available and see what others offer for it.  Could be anything.  Another TV, a couch, gas powered blender, heck, someone might even swap you a car for it.</p>

<p>The possibilities are unlimited.</p>
</h3>

